#!/usr/bin/env python3

# Original array
original_array = [2, -8, 9, 42, 8, 67, -2, 56]

# Create new array by adding 2 to each value
new_array = []

for num in original_array:
    new_array.append(num + 3)

# Display both arrays
print("Original array:", original_array)
print("New array:", new_array)